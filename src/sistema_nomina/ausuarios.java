    void guardar(){
        String usuario,contrasena, tipo;
        String SQL = "nominas";
        
        usuario= usuario_.getText();
        contrasena= contrasena_.getText();
        tipo= tusuario.getSelectedItem().toString();

        
        SQL = "INSERT INTO login (usuario, contrasena, tipo) VALUES (?,?,?)";//los signos de interrogacion estan en espera de la cadena
                                                                                              //que tiene la caja de texto   
        try{
            PreparedStatement pst =  cn.prepareStatement(SQL);//prepara las sentencias SQL pst = nombre de variable SQL es la variable de arriba
            pst.setString(1,usuario);  // nombre de la variable donde gurdamos el TextBox
            pst.setString(2,contrasena); // nombre de la variable donde gurdamos el TextBox
            pst.setString(3,tipo );// nombre de la variable donde gurdamos el TextBox
            
            int n = pst.executeUpdate();
                            //actualizar --- consulta//
         
 
            if (n>=0){//array 
                JOptionPane.showMessageDialog(null,"Datos guardados "); 
                
                usuario_.setText("");
                contrasena_.setText("");
                
            }
            
        }
        catch(SQLException ex){
            Logger.getLogger(alta_usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

